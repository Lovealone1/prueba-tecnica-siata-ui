/**
 * Normalizes a string to Title Case.
 * Example: "UNITED STATES" -> "United States"
 */
export function toTitleCase(str: string): string {
  return str
    .toLowerCase()
    .split(' ')
    .map(word => word.charAt(0).toUpperCase() + word.slice(1))
    .join(' ');
}

export function getCountries(): string[] {
  return Object.keys(COUNTRY_CONTINENT_MAP).map(toTitleCase).sort();
}

export function getContinents(): string[] {
  const continents = new Set(Object.values(COUNTRY_CONTINENT_MAP).map(toTitleCase));
  return Array.from(continents).sort();
}

export function getContinentByCountry(countryName: string): string {
  if (!countryName) return "UNKNOWN";
  const normalized = countryName.trim().toUpperCase();
  const continent = COUNTRY_CONTINENT_MAP[normalized] || "UNKNOWN";
  return toTitleCase(continent);
}
